#function for changing colour
def col(col1,col2,col3):
  s10.color(col1,col2,col3)

#function for drawing line
def dline(lng,ang):
  s10.setheading(ang)
  s10.pendown()
  s10.forward(lng)
  s10.penup()

#drawing function
def line(bob):
  with open("data.txt") as f:
    lines = f.readlines()
  cur = lines[bob].split('-')
  dline(cur[0],cur[1])
  
