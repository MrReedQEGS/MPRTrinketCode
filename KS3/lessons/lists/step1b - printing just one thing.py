myListOfThings = ["cat","dog","horse","spider","ant","bear"]
# POSITIONS         0    1      2        3       4     5

#If I want to print just one thing from a list, then 
# you need to know what POSITION it is in.

# print the thing in position 2 -> horse
print(myListOfThings[2])  

# print the thing in position FIRST thing -> cat
print(myListOfThings[0]) 

# print the thing in position 5 -> bear
print(myListOfThings[5]) 
